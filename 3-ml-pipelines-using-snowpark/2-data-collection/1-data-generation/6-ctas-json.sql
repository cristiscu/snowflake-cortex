CREATE OR REPLACE TABLE DEPT AS
WITH json AS (
    SELECT parse_json('[
        { "DEPTNO": 10, "DNAME": "ACCOUNTING", "LOC": "NEW YORK" },
        { "DEPTNO": 20, "DNAME": "RESEARCH",   "LOC": "DALLAS"   },
        { "DEPTNO": 30, "DNAME": "SALES",      "LOC": "CHICAGO"  },
        { "DEPTNO": 40, "DNAME": "OPERATIONS", "LOC": "BOSTON"   }]'))
SELECT d.value:DEPTNO::int as DEPTNO,
    d.value:DNAME::string as DNAME,
    d.value:LOC::string as LOC
FROM json, TABLE(FLATTEN($1)) d;
ALTER TABLE DEPT ADD CONSTRAINT "pk" PRIMARY KEY (DEPTNO);

